package hola;

public class Consultorio {
	private String consultas,medicos;

	public Consultorio(String consultas, String medicos) {
		super();
		this.consultas = consultas;
		this.medicos = medicos;
	}
	public Consultorio() {
		super();
		this.consultas = "";
		this.medicos = "";
	}
	

	
}
